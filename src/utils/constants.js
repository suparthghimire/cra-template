export const TEXT_REGEX = /[a-zA-Z]/;

export const SPECIAL_CHAR_REGEX =
  /^(?=.*[0-9])(?=.*[!@#$%^&*])[a-zA-Z0-9!@#$%^&*]{6,16}$/;
